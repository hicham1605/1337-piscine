#include <unistd.h>

int	ft_atoi(char *str)
{
	int	count;
	int	result;
	int	i;

	count = 0;
	result = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			count++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	if (count % 2 != 0)
		return (result * (-1));
	return (result);
}
